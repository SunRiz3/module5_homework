let a = prompt ("Введите значение.");
a = +a;
if (a % 2 === 0) {
    alert ("Число чётное.");
} 
else if (a % 1 === 0) {
    alert ("Число нечётное.")
}
else {
    alert ("Упс, кажется, вы ошиблись.")
}
if (a !== a) {
    alert('Введенное значение является NaN.');
} else {
    alert('Введенное значение не является NaN.');
}






